const MENU_FRAGMENT = `#graphql
  fragment MenuItem on MenuItem {
    id
    resourceId
    tags
    title
    type
    url
  }
  fragment ChildMenuItem on MenuItem {
    ...MenuItem
    items {
      ...MenuItem
    }
  }
  fragment ParentMenuItem on MenuItem {
    ...MenuItem
    items {
      ...ChildMenuItem
    }
  }
  fragment Menu on Menu {
    id
    items {
      ...ParentMenuItem
    }
  }
` as const;

export const HEADER_QUERY = `#graphql
  fragment Shop on Shop {
    id
    name
    description
    primaryDomain {
      url
    }
    brand {
      logo {
        image {
          id
          url
          height
          width
          altText
        }
      }
    }
  }
  fragment MetaobjectConnection on MetaobjectConnection {
    nodes {
      handle
      id
      type
      menu_title: field(key: "menu_title") {value}
      title: field(key: "title") {value}
      subtitle: field(key: "subtitle") {value}
      text_color: field(key: "text_color") {value}
      image: field(key: "image") {
        reference {
          ... on MediaImage {
            image {
              id
              url
              height
              width
              altText
            }
          }
        }
      }
      product: field(key: "product") {
        reference {
          ... on Product {
            handle
          }
        }
      }
      collection: field(key: "collection") {
        reference {
          ... on Collection {
            handle
          }
        }
      }
      page: field(key: "page") {
        reference {
          ... on Page {
            handle
          }
        }
      }
    }
  }
  query Header(
    $country: CountryCode
    $headerMenuHandle: String!
    $additionalMenuType: String!
    $language: LanguageCode
  ) @inContext(language: $language, country: $country) {
    shop {
      ...Shop
    }
    menu(handle: $headerMenuHandle) {
      ...Menu
    }
    metaobjects(type: $additionalMenuType, first: 10) {
      ...MetaobjectConnection
    }
  }
  ${MENU_FRAGMENT}
` as const;

